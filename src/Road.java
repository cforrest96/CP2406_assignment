public class Road {
    int road_num;
    int road_seg;
    String road_shape;

    public int get_road_num() {
        return road_num;
    }

    public void set_road_num(int road_num) {
        this.road_num = road_num;
    }

    public int get_road_seg() {
        return road_seg;
    }

    public void set_road_seg(int road_seg) {
        this.road_seg = road_seg;
    }

    public String get_road_shape() {
        return road_shape;
    }

    public void set_road_shape(String road_shape) {
        this.road_shape = road_shape;
    }
}
